void	ft_swap(int *a, int *b)
{
	int temp;

	temp = *a;
	*a = *b;
	*b = temp;
}

#include<stdio.h>

int	main()
{
	int x = 10;
	int y = 5;

	ft_swap(&x,&y);

	printf("x = %d\ny = %d", x,y);
}
