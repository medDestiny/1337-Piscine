int		max(int* tab, unsigned int len)
{
	int	i;
	int	n;

	i = 0;
	n = 0;
	while (i < len)
	{
		if (tab[i] > n)
			n = tab[i];
		i++;
	}
	return (n);
}

#include<stdio.h>

int	main()
{
	int	tab[] = {909,2,55,6,7,8,9999,5,4,0,77,1011};
	int s = 12;
	printf("%d", max(tab, s));
}
