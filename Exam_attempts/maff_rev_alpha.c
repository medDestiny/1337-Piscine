#include<unistd.h>

int	main()
{
	char c;
	int	up;

	c = 'z';
	while (c >= 'a')
	{
		up = c - 32;
		if (c % 2 == 0)
			write(1, &c, 1);
		else
			write(1, &up, 1);
		c--;
	}
	write(1, "\n", 1);
}
