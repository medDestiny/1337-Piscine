#include<unistd.h>

int	main()
{
	char c;
	int	up;
	int	low;

	c = 'a';
	while (c <= 'z' || c <= 'Z')
	{
		up = c -32;
		if (c % 2 == 1)
			write(1, &c, 1);
		else
			write(1, &up, 1);
		c++;
	}
	write(1, "\n", 1);
}
