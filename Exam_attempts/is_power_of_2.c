int	    is_power_of_2(unsigned int n)
{
	int	x;

	x = 2;
	while (x < n)
		x *= 2;
	if (x == n)
		return (1);
	return (0);
}

#include<stdio.h>

int	main()
{
	printf("%d", is_power_of_2(4097));
}
