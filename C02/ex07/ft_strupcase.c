char	*ft_strupcase(char *str)
{
	int	i;
	int	diff;

	i = 0;
	diff = 32;
	while (str[i] != '\0')
	{
		if (str[i] >= 'a' && str[i] <= 'z')
				str[i] = str[i] - diff;
		i++;
	}
	return (str);
}
