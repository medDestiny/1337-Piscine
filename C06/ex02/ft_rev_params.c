#include<unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	x;

	x = ac - 1;
	while (x >= 1)
	{
		i = 0;
		while (av[x][i] != '\0')
		{
			write(1, &av[x][i], 1);
			i++;
		}
		write(1, "\n", 1);
		x--;
	}
}
