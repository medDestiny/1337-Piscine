int	ft_is_prime(int nb)
{
	int	x;

	x = 2;
	if (nb == 0 || nb == 1 || nb < 0)
		return (0);
	while (x <= (nb / x))
	{
		if ((nb % x) == 0)
			return (0);
		x++;
	}
	return (1);
}
